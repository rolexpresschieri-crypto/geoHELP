-- ---------------------------------------------------------------------------
-- 1) profiles — anagrafica base (NON sensibile, in chiaro)
--    Una riga per ogni auth.users.id.
-- ---------------------------------------------------------------------------
create table if not exists public.profiles (
    id                       uuid primary key references auth.users(id) on delete cascade,
    email                    text,                  -- copia da auth.users (login)
    first_name               text,                  -- richiesto al primo onboarding
    birth_year               int  check (birth_year is null or (birth_year between 1900 and 2100)),
    gender                   text check (gender in ('M','F','X') or gender is null),
    emergency_contact_name   text,
    emergency_contact_phone  text,
    preferred_language       text not null default 'it' check (preferred_language in ('it','en')),
    created_at               timestamptz not null default now(),
    updated_at               timestamptz not null default now()
);
-- Nota: blood_group è stato spostato su public.medical_data (vedi sotto)
-- in quanto dato sanitario. Migration: 0006_blood_group_to_medical_data.sql

-- ---------------------------------------------------------------------------
-- 2) medical_data — dati sanitari in chiaro (colonne strutturate)
--    Protetti da RLS (auth.uid() = user_id), trasporto HTTPS e consenso
--    esplicito alla liberatoria. Il team admin (service_role) può leggerli
--    in chiaro per finalità di soccorso/supporto, come da informativa.
--
--    Il PIN locale dell'app serve solo come lock per la schermata Dati medici.
--    Reset/cambio PIN NON tocca i dati su Supabase.
-- ---------------------------------------------------------------------------
create table if not exists public.medical_data (
    user_id          uuid primary key references auth.users(id) on delete cascade,
    conditions       text,                  -- codici patologia separati da virgola (ordine urgenza): CA,PO,NE,PA,PB,DIA,IA,IV,AL
    pacemaker        boolean not null default false,
    blood_group      text check (
                          blood_group is null
                          or blood_group in ('A+','A-','B+','B-','AB+','AB-','0+','0-')
                     ),
    allergies        text,                  -- allergie farmacologiche
    therapies        text,                  -- terapie/farmaci in corso
    notes            text,                  -- note libere
    medical_blob_enc text,                  -- LEGACY (vecchio schema cifrato), non più usato dall'app
    enc_version      int not null default 1,
    updated_at       timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- 3) consents — storico consensi (privacy / dati sanitari / man down)
-- ---------------------------------------------------------------------------
create table if not exists public.consents (
    id            bigint generated by default as identity primary key,
    user_id       uuid not null references auth.users(id) on delete cascade,
    consent_type  text not null check (consent_type in ('privacy','medical_data','man_down')),
    version       text not null,            -- es. 'v1' oppure '2026-05-12'
    accepted      boolean not null,
    accepted_at   timestamptz not null default now()
);

create index if not exists consents_user_id_idx on public.consents (user_id);
create index if not exists consents_type_idx    on public.consents (consent_type);

-- ---------------------------------------------------------------------------
-- 4) sos_recipients — numeri SMS (sostituisce il Google Sheet pubblico)
--    Lettura: tutti gli utenti autenticati.
--    Scrittura: SOLO dashboard / service_role (per ora non si scrive da app).
-- ---------------------------------------------------------------------------
create table if not exists public.sos_recipients (
    id           bigint generated by default as identity primary key,
    label        text,
    phone        text not null,
    role         text not null check (role in ('primary','backup')),
    active       boolean not null default true,
    sort_order   int not null default 0,
    created_at   timestamptz not null default now(),
    updated_at   timestamptz not null default now()
);

create index if not exists sos_recipients_active_idx on public.sos_recipients (active);
create index if not exists sos_recipients_role_idx   on public.sos_recipients (role);

-- ---------------------------------------------------------------------------
-- 5) sms_events — log statistico SMS (senza contenuto messaggio)
--    channel: manual | mandown
--    message_kind: prepared (solo manual) | initial | trace | lost_signal
--    emergency_type: incident | illness | … | mandown
-- ---------------------------------------------------------------------------
create table if not exists public.sms_events (
    id              bigint generated always as identity primary key,
    user_id         uuid not null references auth.users(id) on delete cascade,
    channel         text not null check (channel in ('manual', 'mandown')),
    message_kind    text not null check (message_kind in ('prepared', 'initial', 'trace', 'lost_signal')),
    emergency_type  text not null check (emergency_type in (
                        'incident', 'illness', 'vehicle', 'lost', 'weather', 'other', 'mandown'
                    )),
    outcome         text not null check (outcome in ('ok', 'failed')),
    dest_count      smallint,
    segment_count   smallint,
    trace_index     smallint check (trace_index is null or (trace_index between 1 and 4)),
    recipient_role  text check (recipient_role is null or recipient_role in ('primary', 'backup')),
    created_at      timestamptz not null default now()
);

create index if not exists sms_events_user_id_idx on public.sms_events (user_id);
create index if not exists sms_events_created_at_idx on public.sms_events (created_at desc);
create index if not exists sms_events_channel_idx on public.sms_events (channel);

