-- sos_recipients:
-- - SELECT: tutti (anon + authenticated), solo righe active = true.
--   Motivo: i numeri SOS sono dati operativi NON sensibili (oggi sono pubblicati
--   nel CSV Google Sheet), e l'app deve poterli leggere anche prima del login
--   (es. test, futuro fallback SOS "guest"). Sono comunque solo letture.
