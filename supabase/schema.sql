-- 1) Aggiorna automaticamente updated_at su profiles / medical_data / sos_recipients
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
    new.updated_at = now();
    return new;
end;
$$;

drop trigger if exists trg_profiles_updated_at       on public.profiles;
drop trigger if exists trg_medical_data_updated_at   on public.medical_data;
drop trigger if exists trg_sos_recipients_updated_at on public.sos_recipients;

create trigger trg_profiles_updated_at
    before update on public.profiles
    for each row execute function public.set_updated_at();

create trigger trg_medical_data_updated_at
    before update on public.medical_data
    for each row execute function public.set_updated_at();

create trigger trg_sos_recipients_updated_at
    before update on public.sos_recipients
    for each row execute function public.set_updated_at();

-- 2) Crea automaticamente la riga in public.profiles alla registrazione
--    (così l'app non deve fare il "create profile" la prima volta).
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    insert into public.profiles (id, preferred_language, email)
    values (new.id, 'it', new.email)
    on conflict (id) do update
        set email = excluded.email
        where public.profiles.email is distinct from excluded.email;
    return new;
end;
$$;

drop trigger if exists trg_auth_users_on_signup on auth.users;

create trigger trg_auth_users_on_signup
    after insert on auth.users
    for each row execute function public.handle_new_user();

